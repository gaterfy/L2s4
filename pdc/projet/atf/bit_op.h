/**
 * Flips a bit (0 becomes 1 and vice-versa)
 * @param p a pointer on the bit to flip
 * @param i the index of the bit to flip
 */
void flip_bit(void *,int i);

/**
 * Gets the bit
 * @param p a pointer on the bit to get
 * @param i the index of the bit to get
 * @return the i-th bit
 */
int get_bit(void *,int i);
